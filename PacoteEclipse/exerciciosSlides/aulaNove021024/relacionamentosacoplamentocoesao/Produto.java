package exerciciosSlides.aulaNove021024.relacionamentosacoplamentocoesao;

public class Produto {
	
	  String nome;
	    double preco;

	    public Produto(String nome, double preco) {
	        this.nome = nome;
	        this.preco = preco;
	    }

	    public String getNome() {
	        return nome;
	    }

	    public double getPreco() {
	        return preco;
	    }
}
